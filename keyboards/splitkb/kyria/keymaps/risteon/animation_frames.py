def decode_img(img: List[int]) -> np.ndarray:
    img_width = 128
    img_height = 64
    block_width = 16
    img = np.asarray(img, np.uint8).reshape((img_height, img_width // 8))
    img = np.unpackbits(img[:, None, :], axis=1, bitorder="little")
    img = img.reshape((img_height // 8, img_width // block_width, 8, block_width))
    img = np.moveaxis(img, 1, 2)
    img = img.reshape(img_height, img_width)
    return img


def encode_img(img: np.ndarray) -> List[int]:
    img_height, img_width = img.shape
    block_width = 16
    img = img.reshape(img_height // 8, 8, 128)
    img = np.moveaxis(img, 1, 2)
    img = img.reshape(-1, 8)
    img = np.packbits(img, axis=1, bitorder="little")
    return img.flatten()


def fix_differences(img, base, max_x, min_y, min_x=None):

    coords = np.stack(np.where(img != base), axis=-1)
    
    valid_y = coords[:, 0] > min_y
    valid_x = coords[:, 1] < max_x
    if min_x is not None:
        valid_x &= coords[:, 1] > min_x
    coords = coords[valid_x & valid_y]
    
    for x in coords:
        img[x[0], x[1]] = base[x[0], x[1]]
    

def calc_block_index(y, x):
    """
    * Figure out pixel column -> block column is the same
    * Figure out block row by dividing by 8
    """
    assert x.max() < 128
    assert y.max() < 64
    block_column = x
    block_row = y // 8
    block = block_row * 128 + block_column
    return block


def optimize_block_lengths(change_map, min_length=4):
    # first 0 block is always big enough
    # length of current 0 block
    current_length_0 = min_length
    current_length_1 = 0
    # register whereever a new change block starts
    block_starts = []
    # register lengths of change blocks
    block_lengths = []
    
    for i, v in enumerate(change_map):
        if v == 0:
            current_length_0 += 1
            if current_length_1 > 0:
                block_lengths.append(current_length_1)
                current_length_1 = 0
        else:
            # changed block
            # if we have a current_length, then this is the frist time that we are in a
            # changed block. Need to check the last block of 0
            if current_length_0 > 0:
                # last entry was still 0:
                if current_length_0 >= min_length:
                    # last 0 block is sufficiently long.
                    # This means we need to register a new change block start.
                    block_starts.append(i)
                    # block_lengths.append()
                else:
                    # last 0 block is too short. Need to erase last length
                    current_length_1 = block_lengths[-1] + current_length_0
                    del block_lengths[-1]
                # we took care of the last block. Set length to zero
                current_length_0 = 0
            else:
                # still in a changed block. nothing to do
                pass
        
            current_length_1 += 1
    
    assert len(block_starts) == len(block_lengths)
    return block_starts, block_lengths


def get_block_diffs(img_a, img_b, block_starts, block_lengths):
    img_a = img_a.reshape(-1)
    img_b = img_b.reshape(-1)
    diffs = []
    for start, length in zip(block_starts, block_lengths):
        block_a = img_a[start:start+length]
        block_b = img_b[start:start+length]
        diff = np.bitwise_xor(block_a, block_b)
        diffs.append(diff)
    diffs = np.concatenate(diffs)
    assert len(diffs) == sum(block_lengths)
    return diffs


def print_bytes(data, group_size=16):
    lines = []
    byte_strings = [f"0x{v:02x}" for v in data]
    # group by group_size
    lines = [byte_strings[i:i+group_size] for i in range(0, len(byte_strings), group_size)]
    lines = [", ".join(line) + "," for line in lines]
    return lines


def print_numbers(data, group_size=16):
    lines = []
    byte_strings = [f"{v}" for v in data]
    # group by group_size
    lines = [byte_strings[i:i+group_size] for i in range(0, len(byte_strings), group_size)]
    lines = [", ".join(line) + "," for line in lines]
    return lines


